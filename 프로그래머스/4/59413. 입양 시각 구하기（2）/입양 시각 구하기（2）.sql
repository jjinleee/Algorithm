WITH HOURS (HOUR) AS (
    SELECT 0 AS HOUR FROM DUAL
    UNION ALL
    SELECT HOUR+1 AS HOUR
    FROM HOURS
    WHERE HOUR<23
) 
SELECT HOUR, NVL(COUNT(TO_CHAR(O.DATETIME,'HH24')),0) AS COUNT
FROM HOURS H LEFT JOIN ANIMAL_OUTS O ON H.HOUR=TO_CHAR(O.DATETIME, 'HH24')
GROUP BY HOUR
ORDER BY HOUR